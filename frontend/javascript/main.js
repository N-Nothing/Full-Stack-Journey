// JavaScript Basics: Variables and Data Types

// 1. Variables
let myName = "Jibon"; // String
let age = 25; // Number
let isStudent = true; // Boolean

// 2. Logging values to the console
console.log("Name:", myName);
console.log("Age:", age);
console.log("Is student:", isStudent);

// 3. Data types
let numberExample = 42;
let stringExample = "Hello, world!";
let booleanExample = false;
let arrayExample = [1, 2, 3, 4];
let objectExample = { key: "value", score: 100 };

console.log("Number:", numberExample);
console.log("String:", stringExample);
console.log("Boolean:", booleanExample);
console.log("Array:", arrayExample);
console.log("Object:", objectExample);
